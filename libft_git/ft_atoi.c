int	ft_atoi(const char *nptr)
{
	int	nb;
	int	n;

	n = 1;
	while ((*nptr >= 9 && *nptr <= 13) || *nptr == ' ')
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			n *= -1;
		nptr++;
	}
	nb = 0;
	while (*nptr >= 48 && *nptr <= 57)
	{
		nb = nb * 10 + *nptr - 48;
		nptr++;
	}
	return (n * nb);
}
